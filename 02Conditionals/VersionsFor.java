public class VersionsFor
{
    public static void main(String[] args)
    {
        // using a for loop, incrementing by a larger amount
        for (int i = 0; i <= 50; i += 5)
        {
            System.out.println(i);
        }
        
        // running your for loop backwards by decrementing
        for (int i = 17; i >= 0; i--)
        {
            System.out.println(i);
        }
        
    }
}