public class AlternativeFor
{
    public static void main(String[] args)
    {
        // using a larger increment value
        for (int i = 0; i < 124; i += 17)
        {
            System.out.println(i);
        }
        
        // running your for loop backwards
        for (int i = 16; i >= 0; i--)
        {
            System.out.println(i);
        }
        
    }
}
