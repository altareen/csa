public class DefiniteLoop
{
    public static void main(String[] args)
    {
        // implement a generic for loop
        for (int count = 0; count < 5; count++)
        {
            System.out.println("Hello");    
        }
        
        // another loop using the variable i
        for (int i = 0; i < 10; i++)
        {
            System.out.println(i);
        }
    }
}