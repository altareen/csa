public class ForLoop
{
    public static void main(String[] args)
    {
        // example of a for loop
        for (int count = 0; count < 5; count++)
        {
            System.out.println(count);
        }
        
        // using the variable i for increment
        for (int i = 0; i < 10; i++)
        {
            System.out.println(i);
        }
    }
}