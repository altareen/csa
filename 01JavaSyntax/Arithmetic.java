public class Arithmetic
{
    public static void main(String[] args)
    {
        System.out.println("Demonstrating math operations");
        
        int result = 298 + 347;
        System.out.println(result);
        
        int difference = 109 - 287;
        System.out.println(difference);
        
        double product = 28.1 * 9.37;
        System.out.println(product);
        
        // this performs integer division
        // it is the same as the mathematical floor operation
        double outcome = 10 / 3;
        System.out.println(outcome);
        
        double solution = 10.0 / 3;
        System.out.println(solution);
        
        double output = 10 / 3.0;
        System.out.println(output);
        
        int sum = 10;
        int elements = 3;
        
        double average = 1.0 * sum / elements;
        System.out.println(average);
    }
}