public class Increment
{
    public static void main(String[] args)
    {
        // adding 1 to a value
        int score = 8;
        score = score + 1;
        System.out.println(score);
        
        // increment with the ++ operator
        int count = 5;
        count++;
        System.out.println(count);
        
        // decrement with the -- operator
        int total = 8;
        total--;
        System.out.println(total);
    }
}