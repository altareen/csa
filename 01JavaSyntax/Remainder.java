public class Remainder
{
    public static void main(String[] args)
    {
        // determine the remainder of a division
        int remainder = 17 % 3;
        System.out.println("Remainder: " + remainder);
        
        // determine if a number is even
        int total = 10;
        int result = total % 2;
        boolean outcome = (result == 0);
        System.out.println("Is the number even? " + outcome);
        
        // determine if a number is odd
        int score = 23;
        int solution = score % 2;
        boolean output = (solution == 1);
        System.out.println("Is the number odd? " + output);
    }
}